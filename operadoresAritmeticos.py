# Adição 
numero1 = 10 
numero2 = 20

soma = numero1 + numero2
print(soma)

# Subtraçao

subtracao = numero1 - numero2
print(subtracao)

# Divisao

divisao = numero1 / numero2
print(divisao)

# Multiplicação

multiplicação = numero1 * numero2
print(multiplicação)

# Potencia 

potencia = numero1 ** numero2
print(potencia)

# Modulo 

modulo = numero1 % numero2
print(modulo)

# Resto da divisao 

restodadivisao = numero1 // numero2
print(restodadivisao)